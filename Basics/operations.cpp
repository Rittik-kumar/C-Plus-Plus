#include<bits/stdc++.h>
using namespace std;

int main()
{  //  +,-,*,/,%
   cout<<4+2 << endl;
   cout<<4-2 << endl; 
   cout<<4*2 << endl;
   cout<<4/2 << endl;
   cout<<4%2 << endl;
   cout<<"\n";

   //  ++, --
   int a= 5;
   a= a+1;
   cout<<a << endl;
   cout<<++a << endl;
   cout<<a++ << endl;
   cout<<a << endl;

   cout<<"\n";

   cout<<a << endl;
   cout<<--a << endl;
   cout<<a-- << endl;
   cout<<a << endl;
   
}