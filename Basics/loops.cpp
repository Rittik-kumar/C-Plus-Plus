#include<bits/stdc++.h>
using namespace std;

int main()
{   // while loop
    int i =1;
    while(i<=10)
    {
        cout<< i <<endl;
        i+=1;
    }

    // for loop
    for(int i=1;i<=10;i++)
    {
        cout<< i <<endl;
    }

    // do-while loop
    i=1;
    do{
        cout<< i <<endl;
        i+=1;
    }while(i<=10);
}
